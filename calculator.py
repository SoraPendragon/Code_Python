#Calculator

def add(a,b):
    result = a + b 
    return result

def substract(a,b):
    result = a - b
    return result

def multiply(a,b):
    result = a * b
    return result

def divide(a,b):
    result = a / b
    return result 

def exp(a,b):
    result = a ** b
    return result

print(add(3,4))

print(substract(3,4))

print(multiply(3,4))

print(divide(3,4))

print(exp(3,4))